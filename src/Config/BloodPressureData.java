package Config;

public class BloodPressureData {
	private int SYS;
	private int DIA;
	private int MEAN;
	
	public int getSYS() {
		return SYS;
	}
	public void setSYS(int sYS) {
		SYS = sYS;
	}
	public int getDIA() {
		return DIA;
	}
	public void setDIA(int dIA) {
		DIA = dIA;
	}
	public int getMEAN() {
		return MEAN;
	}
	public void setMEAN(int mEAN) {
		MEAN = mEAN;
	}
}
